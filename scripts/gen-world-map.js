/**
 * One-time generator: world-atlas 110m → SVG paths for admin live map.
 * Run: node scripts/gen-world-map.js
 */
const fs = require('fs');
const path = require('path');
const https = require('https');
const { geoPath, geoEquirectangular } = require('d3-geo');
const { feature } = require('topojson-client');

const W = 720;
const H = 360;
const TOPO_URL = 'https://cdn.jsdelivr.net/npm/world-atlas@2/countries-110m.json';
const ISO_URL = 'https://raw.githubusercontent.com/datasets/country-codes/master/data/country-codes.csv';
const OUT = path.join(__dirname, '..', 'admin', 'world-map-paths.js');

function fetchText(url) {
  return new Promise((resolve, reject) => {
    https.get(url, (res) => {
      if (res.statusCode >= 300 && res.statusCode < 400 && res.headers.location) {
        return fetchText(res.headers.location).then(resolve, reject);
      }
      let data = '';
      res.on('data', (c) => { data += c; });
      res.on('end', () => resolve(data));
    }).on('error', reject);
  });
}

function parseCsvLine(line) {
  const cols = [];
  let cur = '';
  let inQ = false;
  for (let i = 0; i < line.length; i++) {
    const c = line[i];
    if (c === '"') { inQ = !inQ; continue; }
    if (c === ',' && !inQ) { cols.push(cur); cur = ''; continue; }
    cur += c;
  }
  cols.push(cur);
  return cols;
}

function parseIsoCsv(csv) {
  const lines = csv.trim().split('\n');
  const header = parseCsvLine(lines[0]);
  const numIdx = header.indexOf('ISO3166-1-numeric');
  const alphaIdx = header.indexOf('ISO3166-1-Alpha-2');
  const map = {};
  for (let i = 1; i < lines.length; i++) {
    const cols = parseCsvLine(lines[i]);
    const rawNum = cols[numIdx];
    if (!rawNum) continue;
    const num = String(parseInt(rawNum, 10)).padStart(3, '0');
    const alpha = cols[alphaIdx];
    if (num && alpha && alpha.length === 2) map[num] = alpha;
  }
  return map;
}

async function main() {
  const [topoRaw, isoCsv] = await Promise.all([
    fetchText(TOPO_URL).then(JSON.parse),
    fetchText(ISO_URL),
  ]);
  const isoByNum = parseIsoCsv(isoCsv);
  const countries = feature(topoRaw, topoRaw.objects.countries);
  const projection = geoEquirectangular().fitSize([W, H], countries);
  const pathGen = geoPath(projection);

  const paths = countries.features.map((f) => {
    const id = String(f.id || '').padStart(3, '0');
    const code = isoByNum[id] || '';
    const name = (f.properties && f.properties.name) || code || id;
    const d = pathGen(f);
    if (!d) return null;
    return { id, code, name, d };
  }).filter(Boolean);

  const payload = {
    w: W,
    h: H,
    projection: {
      scale: projection.scale(),
      translate: projection.translate(),
    },
    paths,
  };

  const js = '/* Auto-generated by scripts/gen-world-map.js — do not edit */\n' +
    'window.CREDPIX_WORLD_MAP = ' + JSON.stringify(payload) + ';\n';

  fs.writeFileSync(OUT, js, 'utf8');
  console.log('Wrote', OUT, '(' + paths.length + ' countries, scale=' + payload.projection.scale.toFixed(2) + ')');
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
